   <h1>how it works </h1>

   <p>We make this EASY for you!

1. Enter your zip code to see if there is delivery to your area.
2. Create an account to set up your delivery. 
3. Pick out what size of our 3 Farmbox’s you would like. We have different Farmbox’s for different needs!
4. Customize your Farmbox to your liking with up to 5 substitutions.

</p>

BAM! You're done on your way to healthy fresh, organic produce and grocery items delivered right to your door!

There is NO commitment ever! If you are going on vacation, or just need to skip a delivery, our system makes it very easy for you to manage your delivery, or feel free to email us anytime to assist you. 

We do not charge a fee for registration or membership! 

Minimum order is $38.95 and we have a $5.98 handling and packing fee. All of our boxes and packing material are biodegradable / recyclable.

*Each week we also send a recipe card with each farmbox giving you neat ideas for cooking with that weeks box!*

 

How It Works:

At the beginning of each week we send out an email letting you know what is being harvested by our farmers for the current week’s Farmbox.  You then can go into your account and customize your Farmbox with up to 5 substitutions, per Farmbox. You then can add Artisanal items, and other grocery items we offer to come with your Farmbox delivery.  The cut off is Wednesday at 12PM EST to skip or cancel an order for the next week. The cut off Ito make subs is Friday at 10am EST. We then deliver your Farmbox to your door in a box that is Eco friendly! Please be sure to recycle your boxes and packing material.

 

We use UPS for our delivery needs and provide all customers with tracking information. Unfortunatley we are unable to deliver to PO Boxes. We use insulation that is biodegradable and ice packs to keep your box at the perfect temperature and insure safe delivery.

 

Delivery Schedule
 
We deliver on different days to different zip codes.  Please input your zip code to find out your delivery day. Once the box leaves us it is in the hands of UPS.  Your credit card will be charged on Friday at 10am EST. We have a cut off because we must tell our Farmers and Artisans what we need for each delivery. Don’t worry, if you miss this week’s cut off, be sure to still create an account so you are on our email list for the following week's harvest!  
 
*Please note: Delivery delay can be at the hands of Mother Nature due to weather, or traffic delays, once the order leaves us it is in the hands of UPS. All damage reports must be made to hello@farmboxdirect.com within 24 hours of receiving your order and pictures may be requested*   
 


